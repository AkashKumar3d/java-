package com.company;

public class ak04_literals {
    public static void main(String[] args) {
        int age=20;
        short age1=21;
        long sallary=222536586455l;
        byte age2= 25;
        float age3=30.4f;// float ke liye hame  value ke saat f bhi likhana padata hai .
        double age4=2020.365d;// double by default literal hota hai
        char name='a';
        boolean a= true;
        String  str="akash";// string ko yese  assign karate hai
        System.out.println(age);
        System.out.println(age1);
        System.out.println(sallary);
        System.out.println(age2);
        System.out.println(age3);
        System.out.println(age4);
        System.out.println(name);
        System.out.println(a);
        System.out.println(str);
    }
}

