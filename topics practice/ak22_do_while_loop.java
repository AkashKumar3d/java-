/*
------------------->do while loop<--------------------
1)do while loop like a while loop but do while loop execute
the code one time and then check the condition;
 */

public class ak22_do_while_loop {
    public static void main(String[] args) {
        int a=0;
        do{
            System.out.println(a);
            a++;
        }while (a<=20);
    }
}
